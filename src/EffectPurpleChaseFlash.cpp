void EffectEffectPurpleChaseFlash::Run(CRGB* strip, uint64_t systime)
{
   
    uint64_t state = (systime / 200) %  STAGES;
    for(int i = 0 ; i < FastLED.size() ; i++)
    {
        if(LightStages[state][i])
        {
            strip[i]=CRGB(255,0,255);
        }
        else
        {
            strip[i]=CRGB(0,0,0);
        }
    }
}

